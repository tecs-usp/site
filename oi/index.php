    <div class="row">
      <div class="col-md-8 explicacoes">
        <p>O projeto foi idealizado em 2019. Ele nasce não só da necessidade de incentivar mais mulheres na carreira de computação, mas também um perfil específico de mulheres: as de baixa renda.</p>
        <p>Entretanto, como muitas escolas públicas não têm salas de computadores, temos dificuldades de levar o curso para as escolas. Mas, como várias meninas já vem à USP para ter aulas nos cursinhos populares, percebemos que, para uma primeira iniciativa, não precisávamos ir até os bairros para atingi-las.</p>
        <p>Assim nasce o Vestibulandas: o projeto do Tecs que visa dar aulas de programação em Python para as alunas dos cursinhos populares da USP utilizando a própria estrutura da Universidade. Python foi a linguagem escolhida não só por ser a de maior parte dos integrantes no momento, como também a linguagem mais usada no mundo nos cursos de introdução.</p>
